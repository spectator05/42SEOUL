void	ft_rev_int_tab(int *tab, int size)
{
	int idx;
	int temp;

	idx = 0;
	while (idx < (size / 2))
	{
		temp = tab[idx];
		tab[idx] = tab[(size - idx) - 1];
		tab[(size - 1) - idx++] = temp;
	}
}
