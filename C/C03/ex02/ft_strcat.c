char	*ft_strcat(char *dest, char *src)
{
	int	i;

	i = 0;
	while (*dest != '\0')
	{
		dest++;
		i++;
	}
	while (*src != '\0')
	{
		*dest++ = *src++;
		i++;
	}
	*dest = *src;
	while (i != 0)
	{
		dest--;
		i--;
	}
	return (dest);
}
