#include <unistd.h>

int main(int argc, char **argv)
{
	int i;

	i = argc - 1;
	while (0 < i)
	{
		while (*argv[i])
			write(1, argv[i]++, 1);
		i--;
		write(1, &"\n", 1);
	}
}
